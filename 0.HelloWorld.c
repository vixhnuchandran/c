#include <stdio.h>

int main(){

    // This is a single-line comment
    printf("Hello, World!\n");  // This prints a message

    /* 
    This is a multi-line comment
    It can span several lines
    */
    printf("Bye World!\n");
    return 0;
}



/* 
    \n	Newline (line break)
    \t	Horizontal tab
    \\	Backslash (\)
    \"	Double quote (")
    \'	Single quote (')
    \r	Carriage return
    \b	Backspace
    \f	Form feed
    \0	Null character
*/

